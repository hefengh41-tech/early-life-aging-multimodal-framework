mask_gene_f32 = mask_gene_npy.astype("float32")  # (748, 884)

IN_DIM_CPG   = mask_cpg_f32.shape[0]
OUT_DIM_GENE = mask_cpg_f32.shape[1]
OUT_DIM_PATH = mask_gene_f32.shape[1]

# ---------- 3. Define BINN Model -------------------------------------
@register_keras_serializable()
class UpgradedBINN(Model):
    def __init__(self,
                 mask_cpg_gene: tf.Tensor,
                 mask_gene_path: tf.Tensor,
                 dropout_rate: float = 0.30):
        super().__init__(name="UpgradedBINN")

        self.mask_cpg_gene  = tf.constant(mask_cpg_gene,  dtype=tf.float32)
        self.mask_gene_path = tf.constant(mask_gene_path, dtype=tf.float32)

        self.W_cpg_gene = self.add_weight(
            name="W_cpg_gene",
            shape=self.mask_cpg_gene.shape,
            initializer=tf.keras.initializers.RandomNormal(stddev=0.05),
            trainable=True)
        self.b_gene = self.add_weight(
            name="b_gene",
            shape=(OUT_DIM_GENE,),
            initializer="zeros",
            trainable=True)

        self.W_gene_path = self.add_weight(
            name="W_gene_path",
            shape=self.mask_gene_path.shape,
            initializer=tf.keras.initializers.RandomNormal(stddev=0.05),
            trainable=True)
        self.b_pathway = self.add_weight(
            name="b_pathway",
            shape=(OUT_DIM_PATH,),
            initializer="zeros",
            trainable=True)

        self.bn_gene   = layers.BatchNormalization()
        self.bn_path   = layers.BatchNormalization()
        self.drop_gene = layers.Dropout(rate=dropout_rate)
        self.drop_path = layers.Dropout(rate=dropout_rate)

    def call(self, inputs, training=False):
        gene_act = tf.matmul(inputs, self.W_cpg_gene * self.mask_cpg_gene) + self.b_gene
        gene_act = tf.nn.relu(gene_act)
        gene_act = self.bn_gene(gene_act, training=training)
        gene_act = self.drop_gene(gene_act, training=training)

        path_act = tf.matmul(gene_act, self.W_gene_path * self.mask_gene_path) + self.b_pathway
        path_act = self.bn_path(path_act, training=training)
        path_act = self.drop_path(path_act, training=training)

        return tf.nn.tanh(path_act)

