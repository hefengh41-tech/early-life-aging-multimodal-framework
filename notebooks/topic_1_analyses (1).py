# ── 2  helper: load a mask CSV robustly ─────────────────────────────────
def load_mask(path: str, label: str) -> np.ndarray:
    """
    Loads a 0/1 mask from CSV.
      • Drops a text first column if present.
      • Converts non-numeric entries to 0.
      • Returns float32 NumPy array.
    """
    df = pd.read_csv(path, low_memory=False)

    # If the first column is non-numeric (IDs), drop it.
    if not pd.api.types.is_numeric_dtype(df.iloc[:, 0]):
        df = df.drop(df.columns[0], axis=1)

    arr = (
        df                     # DataFrame
        .apply(pd.to_numeric, errors="coerce")
        .fillna(0)
        .values
        .astype(np.float32)
    )
    density = arr.mean()
    print(f"{label:18s} → shape {arr.shape},   density {density:.4f}")
    return arr

